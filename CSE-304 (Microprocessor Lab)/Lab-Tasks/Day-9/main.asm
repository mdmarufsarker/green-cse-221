INCLUDE 'EMU8086.INC'

.MODEL SMALL
.DATA
.CODE

MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    PRINT 'ENTER A NUMBER: '
    CALL SCAN_NUM
    MOV AX, CX
    PRINTN ''
    PRINT 'ENTER THE DIVISION: '
    CALL SCAN_NUM
    DIV CX
   
    PRINTN ''
    PRINT 'RESULT: '
    CALL PRINT_NUM
    PRINT '.'
    MOV AX, DX
    CALL PRINT_NUM

    EXIT:
	MOV AH, 4CH
	INT 21H

MAIN ENDP

DEFINE_SCAN_NUM
DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS
DEFINE_GET_STRING
