INCLUDE 'EMU8086.INC'

.MODEL SMALL
.DATA
.CODE

MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    PRINT 'WE ARE IN MAIN PROCEDURE'
    PRINTN ''
    CALL PROCEDURE_TEST

    PRINT 'MAIN, ENTER A NUMBER: '
    CALL SCAN_NUM
    MOV AX, CX
    PRINTN ''
    PRINT 'MAIN, YOU ENTERED: '
    CALL PRINT_NUM

    EXIT:
    MOV AH, 4CH
    INT 21H
MAIN ENDP

PROCEDURE_TEST PROC
    PRINTN 'WE ARE IN PROCEDURE_TEST PROCEDURE'

    RET
PROCEDURE_TEST ENDP

DEFINE_SCAN_NUM
DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS
END MAIN
