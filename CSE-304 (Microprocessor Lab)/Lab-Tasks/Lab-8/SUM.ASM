INCLUDE 'EMU8086.INC'
.MODEL SMALL
.STACK 100H

.DATA
    N DW ?
    E_SUM DW 0
    O_SUM DW 0

.CODE
; MACRO USED FOR PASSING ARGUMENTS TO A PROCEDURE/FUNCTION

RANGE MACRO N
    PRINT 'ENTER THE RANGE VALUE: $'
    CALL SCAN_NUM
    MOV N, CX
ENDM

MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    RANGE N
    CALL EVEN_SUM
    CALL ODD_SUM

    EXIT:
    MOV AH, 4CH
    INT 21H
MAIN ENDP

EVEN_SUM PROC
    MOV AX, N
    PRINTN ''
    CALL PRINT_NUM

    RET
EVEN_SUM ENDP

ODD_SUM PROC
    MOV AX, N
    PRINTN ''
    CALL PRINT_NUM
    
    RET
ODD_SUM ENDP

DEFINE_SCAN_NUM
DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS
END MAIN
